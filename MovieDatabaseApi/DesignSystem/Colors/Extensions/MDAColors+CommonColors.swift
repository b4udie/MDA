import UIKit

/// Palette of common colors
extension MDAColors {

    /**
     HEX - **#000000**
     RGB - **RGB(0, 0, 0)**
     */
    var black: UIColor { return #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1) }

    /**
     HEX - **#FFFFFF**
     RGB - **RGB(255, 255, 255)**
     */
    var white: UIColor { return #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1) }

    /**
     HEX - **#1D1C1E**
     RGB - **RGB(29, 28, 30)**
     */
    var eerieBlack: UIColor { return #colorLiteral(red: 0.1137254902, green: 0.1098039216, blue: 0.1176470588, alpha: 1) }
    
    /**
     HEX - **#AAAAAA**
     RGB - **RGB(170, 170, 170)**
     */
    var darkGray: UIColor { return #colorLiteral(red: 0.6666666667, green: 0.6666666667, blue: 0.6666666667, alpha: 1) }

    /**
     HEX - **#8D8D8E**
     RGB - **RGB(141, 141, 142)**
     */
    var philippineGray: UIColor { return #colorLiteral(red: 0.5529411765, green: 0.5529411765, blue: 0.5568627451, alpha: 1) }

    /**
     HEX - **#FD9D0A**
     RGB - **RGB(253, 157, 10)**
     */
    var orange: UIColor { return #colorLiteral(red: 0.9921568627, green: 0.6156862745, blue: 0.03921568627, alpha: 1) }
    
    /**
     HEX - **#121212**
     RGB - **RGB(18, 18, 18)**
     */
    var chineseBlack: UIColor { return #colorLiteral(red: 0.07058823529, green: 0.07058823529, blue: 0.07058823529, alpha: 1) }
    
    /**
     HEX - **#5A5A5F**
     RGB - **RGB(90, 90, 95)**
     */
    var ebony: UIColor { return #colorLiteral(red: 0.3529411765, green: 0.3529411765, blue: 0.3725490196, alpha: 1) }
}
